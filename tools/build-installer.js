"use strict";

const fs = require("fs");
const path = require("path");
const { execSync, spawnSync } = require("child_process");

const root = path.resolve(__dirname, "..");
const distRoot = path.join(root, "dist");
const appFolderName = "PulseForge-Console-Windows";
const appFolder = path.join(distRoot, appFolderName);
const installerWorkDir = path.join(distRoot, "installer");
const issPath = path.join(installerWorkDir, "PulseForge-Console.iss");

function run(command, cwd = root) {
  execSync(command, { cwd, stdio: "inherit" });
}

function ensureDir(target) {
  fs.mkdirSync(target, { recursive: true });
}

function findIsccExe() {
  const whereResult = spawnSync("cmd", ["/c", "where iscc"], { encoding: "utf8" });
  if (whereResult.status === 0) {
    const first = String(whereResult.stdout || "").split(/\r?\n/).map((line) => line.trim()).find(Boolean);
    if (first && fs.existsSync(first)) {
      return first;
    }
  }

  const candidates = [
    "C:/Program Files (x86)/Inno Setup 6/ISCC.exe",
    "C:/Program Files/Inno Setup 6/ISCC.exe",
    process.env.LOCALAPPDATA
      ? path.join(process.env.LOCALAPPDATA, "Programs", "Inno Setup 6", "ISCC.exe")
      : null
  ];

  return candidates.filter(Boolean).find((candidate) => fs.existsSync(candidate)) || null;
}

function writeIssScript() {
  ensureDir(installerWorkDir);
  const escapedDist = appFolder.replace(/\\/g, "\\\\");
  const escapedOutput = installerWorkDir.replace(/\\/g, "\\\\");

  const iss = `; Generated by tools/build-installer.js
[Setup]
AppId={{26D52CC6-7EC0-4A8B-B25F-84291A3CB57B}
AppName=PulseForge Console
AppVersion=2.1.0
AppPublisher=PulseForge
DefaultDirName={autopf}\\PulseForge Console
DefaultGroupName=PulseForge Console
DisableProgramGroupPage=yes
OutputDir=${escapedOutput}
OutputBaseFilename=PulseForge-Console-Setup
Compression=lzma
SolidCompression=yes
WizardStyle=modern
PrivilegesRequired=admin

[Languages]
Name: "chinesesimp"; MessagesFile: "compiler:Default.isl"

[Tasks]
Name: "desktopicon"; Description: "创建桌面快捷方式"; GroupDescription: "附加任务:"; Flags: unchecked

[Files]
Source: "${escapedDist}\\*"; DestDir: "{app}"; Flags: recursesubdirs createallsubdirs

[Icons]
Name: "{group}\\启动 PulseForge Console"; Filename: "{app}\\启动监控.bat"
Name: "{group}\\停止 PulseForge Console"; Filename: "{app}\\停止监控.bat"
Name: "{group}\\使用说明"; Filename: "{app}\\使用说明-分发版.md"
Name: "{autodesktop}\\PulseForge Console"; Filename: "{app}\\启动监控.bat"; Tasks: desktopicon

[Run]
Filename: "{app}\\启动监控.bat"; Description: "安装完成后立即启动"; Flags: nowait postinstall skipifsilent
`;

  fs.writeFileSync(issPath, iss, "utf8");
}

function main() {
  console.log("[package:installer] building portable package first...");
  run("node tools/package-win.js");

  if (!fs.existsSync(appFolder)) {
    throw new Error(`Distribution folder missing: ${appFolder}`);
  }

  writeIssScript();

  const isccExe = findIsccExe();
  if (!isccExe) {
    console.log("[package:installer] Inno Setup not found.");
    console.log("[package:installer] Please install Inno Setup 6: https://jrsoftware.org/isinfo.php");
    console.log(`[package:installer] ISS script generated at: ${issPath}`);
    process.exit(2);
  }

  console.log(`[package:installer] using ISCC: ${isccExe}`);
  run(`\"${isccExe}\" \"${issPath}\"`);

  const setupExe = path.join(installerWorkDir, "PulseForge-Console-Setup.exe");
  if (!fs.existsSync(setupExe)) {
    throw new Error("Installer build finished but setup exe not found.");
  }

  console.log(`[package:installer] done: ${setupExe}`);
}

main();
